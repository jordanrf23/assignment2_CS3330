package mu.edu.classDesign;

public abstract class MiddleEarthCharacters {
	
	public String name;
	public double health = 0.0;
	public double power = 0.0;
	public MiddleEarthCharacters(String name, double health, double power) {
		super();
		this.name = name;
		this.health = health;
		this.power = power;
	}
	

}
